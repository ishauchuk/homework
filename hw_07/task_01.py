def inch_to_cm(n):
    return n * 2.54


def cm_to_inch(n):
    return n * 0.394


def miles_to_km(n):
    return n * 1.609


def km_to_miles(n):
    return n * 0.621


def lb_to_kg(n):
    return n * 0.454


def kg_to_lb(n):
    return n * 2.204


def oz_to_g(n):
    return n * 28.350


def g_to_oz(n):
    return n * 0.035


def gallon_to_l(n):
    return n * 3.785


def l_to_gallon(n):
    return n * 0.264


def pint_to_l(n):
    return n * 0.568


def l_to_pint(n):
    return n * 1.76